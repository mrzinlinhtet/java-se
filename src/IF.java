public class IF {
    public static void main(String[] args) {
        int age = 13;

        if(age == 75) {
            System.out.println("OK boomer!");
        }
        else if (age >=18) {
            System.out.println("You are adult");
        }
        else if (age >=13) {
            System.out.println("You are teenager!");
        }
        else {
            System.out.println("You are not an adult!");
        }
    }
}
